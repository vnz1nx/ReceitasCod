package Receitas.com.br;

public class Receita {

	private String nome;
	private String tipo;
	private int id;

	public Receita() {
		super();
		this.nome = nome;
		this.tipo = tipo;
		this.id = id;
	}

	public String getNome() {
		return nome;
	}

	public void setNome(String nome) {
		this.nome = nome;
	}

	public String getTipo() {
		return tipo;
	}

	public void setTipo(String tipo) {
		this.tipo = tipo;
	}

	public int getId() {
		return id;
	}

	public void setId(int id) {
		this.id = id;
	}
	
	

}
